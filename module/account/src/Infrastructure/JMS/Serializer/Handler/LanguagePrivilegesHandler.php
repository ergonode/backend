declare(strict_types=1);

namespace Ergonode\Account\Infrastructure\JMS\Serializer\Handler;

use Ergonode\Core\Domain\ValueObject\LanguagePrivileges;
use JMS\Serializer\Context;
use JMS\Serializer\GraphNavigatorInterface;
use JMS\Serializer\Handler\SubscribingHandlerInterface;
use JMS\Serializer\Visitor\DeserializationVisitorInterface;
use JMS\Serializer\Visitor\SerializationVisitorInterface;

class LanguagePrivilegesHandler implements SubscribingHandlerInterface
{
    /**
     * @return array
     */
    public static function getSubscribingMethods(): array
    {
        $methods = [];
        $formats = ['json', 'xml', 'yml'];

        foreach ($formats as $format) {
            $methods[] = [
                'direction' => GraphNavigatorInterface::DIRECTION_SERIALIZATION,
                'type' => LanguagePrivileges::class,
                'format' => $format,
                'method' => 'serialize',
            ];

            $methods[] = [
                'direction' => GraphNavigatorInterface::DIRECTION_DESERIALIZATION,
                'type' => LanguagePrivileges::class,
                'format' => $format,
                'method' => 'deserialize',
            ];
        }

        return $methods;
    }

    /**
     * @param array $type
     *
     * @return array
     */
    public function serialize(
        SerializationVisitorInterface $visitor,
        LanguagePrivileges $languagePrivilege,
        array $type,
        Context $context
    ): array {
        return ['read' => $languagePrivilege->isReadable(), 'edit' => $languagePrivilege->isEditable()];
    }

    /**
     * @param mixed $data
     * @param array $type
     */
    public function deserialize(
        DeserializationVisitorInterface $visitor,
        $data,
        array $type,
        Context $context
    ): LanguagePrivileges {
        return new LanguagePrivileges($data['read'], $data['edit']);
    }
}
